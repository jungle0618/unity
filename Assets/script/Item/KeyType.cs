/// <summary>
/// 鑰匙類型枚舉
/// 用於定義不同顏色的鑰匙
/// </summary>
public enum KeyType
{
    None = 0,        // 無鑰匙（不需要鑰匙）
    Red = 1,         // 紅色鑰匙
    Blue = 2,        // 藍色鑰匙
    Green = 3,       // 綠色鑰匙
    Yellow = 4,      // 黃色鑰匙
    Purple = 5,      // 紫色鑰匙
    Orange = 6,      // 橙色鑰匙
    White = 7,       // 白色鑰匙
    Black = 8,       // 黑色鑰匙
    Master = 99      // 萬能鑰匙（可以開啟任何門）
}

